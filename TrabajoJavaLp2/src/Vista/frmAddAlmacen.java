    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        // TODO add your handling code here:
        if(evt.getKeyChar()==KeyEvent.VK_SPACE && jTextField1.getText().endsWith(" "))
            evt.consume();
        if(evt.getKeyChar()==KeyEvent.VK_SPACE && jTextField1.getText().isEmpty())
            evt.consume();
        
        String s1=String.valueOf(evt.getKeyChar());
        if(evt.getKeyChar()!=WCKeyEvent.VK_BACK && evt.getKeyChar()!=KeyEvent.VK_SPACE){
            if(!s1.matches("[aA-zZ0-9.]")){
                evt.consume();
                getToolkit().beep();
                
            }
        }
    }//GEN-LAST:event_jTextField1KeyTyped

    private void jSpinner1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jSpinner1KeyTyped
        // TODO add your handling code here:
        String s1=String.valueOf(evt.getKeyChar());
        if(evt.getKeyChar()!=WCKeyEvent.VK_BACK){
            if(jSpinner1.getText().length()>=5){
                evt.consume();
            
            }
            if(!s1.matches("[0-9]")){
                evt.consume();
                getToolkit().beep();
                
            }
        }
    }//GEN-LAST:event_jSpinner1KeyTyped

