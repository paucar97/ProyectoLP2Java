    public void login(){
        String usuario=txtUser.getText().toString();
        char[]pass=txtPass.getPassword();
        String contra=String.copyValueOf(pass);
        boolean encontrado=false;
        ArrayList<Usuario>usuarios=new ArrayList<Usuario>();
        UsuarioBL userBL=new UsuarioBL();  
        try{    
            usuarios=userBL.listarUsuarios();
            for(int i=0;i<usuarios.size();i++){
                if(usuario.compareTo("")!=0 && usuario.compareTo(usuarios.get(i).getIdUsuario())==0){
                    encontrado=true;
                    if(contra.equals(usuarios.get(i).getContraseña())){
                        username=usuarios.get(i).getIdUsuario().toString();
                        tipoUser=usuarios.get(i).getTipo();
                        respuesta=usuarios.get(i).getRespuestaC().toString();
                        if(usuarios.get(i).getIngreso()==0){
                        if(tipoUser==0){
                            userBL.iniciarSesion(usuarios.get(i).getIdUsuario(),1);
                            frmPanel panel=new frmPanel(this,false);

                            this.dispose();
                            panel.setVisible(true);
                        }
                        else if(tipoUser==2){
                            
                            userBL.iniciarSesion(usuarios.get(i).getIdUsuario(),1);
                            frmPanelOperario panel=new frmPanelOperario(this,false);

                            this.dispose();
                            panel.setVisible(true);
                        }
                        else JOptionPane.showMessageDialog(this, 
                        "Usted no cuenta con los privilegios necesarios para ingresar","Advertencia",
                        WARNING_MESSAGE);
                        }else JOptionPane.showMessageDialog(this, 
                        "El usuario ya se encuentra logeado","Advertencia",
                        WARNING_MESSAGE);
                    }
                    else{
                        JOptionPane.showMessageDialog(this, 
                        "Contraseña incorrecta","Advertencia",
                        WARNING_MESSAGE);
                        break;
                    }
                }
            }
            if(!encontrado){
                JOptionPane.showMessageDialog(this, 
                  "Usuario incorrecto","Advertencia",
                  WARNING_MESSAGE);
            }
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        
    }

